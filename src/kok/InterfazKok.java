    public void dibujar() {
        if (!started) {
            dibuj = new InterfazKok.Dibujo2D();
            dibuj.setBounds(new Rectangle(PREFERED_WIDTH, PREFERED_HEIGHT));
            dibuj.setOpaque(false);
            started = true;
        }
        jPanel.add(dibuj);
        jPanel.repaint();
    }

